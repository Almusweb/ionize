<?php

class Settings
{
	/* ------------------------------------------------------------------------------------------------------------- */
	public static $instance = NULL;
	public $cache_time = 60 * 60 * 1;
	/* ------------------------------------------------------------------------------------------------------------- */
	
	public function __construct()
	{
		self::$instance =& $this;
	}
	/* ------------------------------------------------------------------------------------------------------------- */
	
	public static function get( $setting )
	{
		return self::$instance->$setting;
	}
	/* ------------------------------------------------------------------------------------------------------------- */
}
